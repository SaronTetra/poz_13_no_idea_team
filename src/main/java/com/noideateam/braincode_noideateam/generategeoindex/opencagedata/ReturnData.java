package com.noideateam.braincode_noideateam.generategeoindex.opencagedata;

public class ReturnData {
    private float x;
    private float y;

    public ReturnData(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
